	dot_lhs.y = lhs->m[3][1];
	dot_lhs.z = lhs->m[3][2];
	dot_lhs.w = lhs->m[3][3];

	dot_rhs.x = rhs->m[0][0];
	dot_rhs.y = rhs->m[1][0];
	dot_rhs.z = rhs->m[2][0];
	dot_rhs.w = rhs->m[3][0];

	dot_sse( &dot_lhs, &dot_rhs, &out->m[3][0] );

	dot_lhs.x = lhs->m[3][0];
	dot_lhs.y = lhs->m[3][1];
	dot_lhs.z = lhs->m[3][2];
